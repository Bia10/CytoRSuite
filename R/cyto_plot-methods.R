cyto_plot.GatingHierarchy <- function(x,
                                      parent,
                                      alias = NA,
                                      channels,
                                      axes_trans = NA,
                                      overlay = NA,
                                      gate = NA,
                                      limits = "machine",
                                      display = 1,
                                      popup = FALSE,
                                      xlim = NA,
                                      ylim = NA,
                                      title,
                                      xlab,
                                      ylab,
                                      density_modal = TRUE,
                                      density_smooth = 0.6,
                                      density_stack = 0.5,
                                      density_cols = NA,
                                      density_fill = NA,
                                      density_fill_alpha = 1,
                                      density_line_type = 1,
                                      density_line_width = 1,
                                      density_line_col = "black",
                                      point_shape = ".",
                                      point_size = 2,
                                      point_col_scale = NA,
                                      point_cols = NA,
                                      point_col = NA,
                                      point_col_alpha = 1,
                                      contour_lines = 0,
                                      contour_line_type = 1,
                                      contour_line_width = 1,
                                      contour_line_col = "black",
